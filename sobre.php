<h1>Sobre</h1>
<p>
O portal Ciência Já oferece conteúdo pré-vestibular na área de ciências naturais e exatas, que inclui vídeo aulas, jogos e animações. Além de preparar o vestibulando para as provas, o portal busca aproximar esses jovens do mundo da ciência.
</p>
<p>
	Vemos a ciência como o caminho para um país mais desenvolvido e democrático, por isso buscamos apresentá-la de forma dinâmica e com uma linguagem acessível, buscando motivar uma quantidade cada vez maior de jovens a seguir a carreira de cientista.
</p>
<p>
	Dessa forma além do conteúdo pré-vestibular o estudante encontrará aqui curiosidades do mundo da ciência, divulgação de pesquisas e cursos das universidades brasileiras.
</p>
